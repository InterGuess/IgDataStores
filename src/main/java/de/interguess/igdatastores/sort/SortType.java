package de.interguess.igdatastores.sort;

/**
 * This enum is used to specify the sort type of objects in a datastore.
 * The sort type can be used to sort the objects in ascending or descending order.
 */
public enum SortType {

    /**
     * This type is used to sort the data in ascending order.
     */
    ASCEND,

    /**
     * This type is used to sort the data in descending order.
     */
    DESCEND
}
